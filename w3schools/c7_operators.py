# ARITHMETIC OPERATORS:
#   +, -, *, /, //, %, **

# Modulus
print(10 % 3) # 1

# Exponentiation
print(2 ** 5) # 2 to the power of 5

# Floor Division
print(10 // 3) # 3

# ASSIGNMENT OPERATORS:
#   =, +=, -=, *=, /=, //=, %=, **=, &=, |=, ^=, >>=, <<=

# COMPARISON OPERATORS:
#   ==, !=, >, <, >=, <=

# LOGICAL OPERATORS:
#   and, or, not
print(True and False) # False
print(True or False) # True
print(not True) # False

# IDENTITY OPERATORS:
#   is, is not
print(1 is 1) # True
print(1 is not 1) # False

# MEMBERSHIP OPERATORS:
#   in, not in
print(1 in [1, 2, 3]) # True
print(1 not in [1, 2, 3]) # False

# BITWISE OPERATORS:
#   &, |, ^, ~, <<, >>
print('AND:', 1 & 3) # 1
print('OR:', 1 | 3) # 3
print('XOR:', 1 ^ 3) # 2
print('NOT:', ~1) # -2
print('LEFT SHIFT:', 1 << 2) # 4
print('RIGHT SHIFT:', 1 >> 2) # 0.5